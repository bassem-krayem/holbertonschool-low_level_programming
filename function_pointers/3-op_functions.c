#include "3-calc.h"

int op_add(int a, int b)
{
	int result = a + b;

	return (result);
}

int op_sub(int a, int b)
{
	int result = a - b;

	return (result);
}

int op_mul(int a, int b)
{
	int result = a * b;

	return (result);
}

int op_div(int a, int b)
{
	int result = a / b;

	return (result);
}

int op_mod(int a, int b)
{
	int result = a % b;

	return (result);
}
